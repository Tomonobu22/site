export function createBoard(grid) {
    const squares = [];

    layout.forEach((cell, i) => {
        const square = document.createElement('div');
        grid.appendChild(square);
        squares.push(square);

        const classMap = {
            0: 'pac-dot',
            1: 'wall',
            2: 'ghost-lair',
            3: 'power-pellet'
        };

        if (classMap[cell]) {
            square.classList.add(classMap[cell]);
        }
    });

    return squares;
}